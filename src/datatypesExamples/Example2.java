package datatypesExamples;

public class Example2 {
	public static void main(String args[]) 
	{
		//boolean datatype
		boolean a = 30>10;
		System.out.println(a);
		
		
		//byte datatype
		byte b = 120;
		System.out.println(b);
		
		
		//short datatype
		short c = 29444;
		System.out.println(c);
		
		// integer datatype
		int d = 455657845;
		System.out.println(d);
		
		//long datatype
		long e = 2324335346787l;
		System.out.println(e);
		
		
		//float datatype
		float f = 1234.566f;
		System.out.println(f);
		
		//double datatype
		double g = 23877.8748;
		System.out.println(g);
		
		// char datatype
		char h = 'A';
		System.out.println(h);
		
		//String datatype
		String i = "My Name is Mayur";
		System.out.println(i);
		
		
	}
}
