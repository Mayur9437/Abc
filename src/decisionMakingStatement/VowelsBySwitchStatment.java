package decisionMakingStatement;

public class VowelsBySwitchStatment {

	public static void main(String[] args) {
		
		char ch  = 'o';
		
		switch (ch )
		{
			case 'a' : System.out.println("Character "+ch+" is Vowel");break;
			case 'e' : System.out.println("Character "+ch+" is Vowel");break;
			case 'i' : System.out.println("Character "+ch+" is Vowel");break;
			case 'o' : System.out.println("Character "+ch+" is Vowel");break;
			case 'u' : System.out.println("Character "+ch+" is Vowel");break;
			default : System.out.println("Character "+ch+" is Consonent");
		}
		
		
	}

}
