package opratorsExamples;

public class RelationalOprators {
	
	// main method
	public static void main(String[] args) {
		
		int a = 20;
		int b = 30;
		
		// Relational Operators 
		System.out.println(a>b);
		System.out.println(a<b);
		System.out.println(a>=b);
		System.out.println(a<=b);
		System.out.println(a==b);
		System.out.println(a!=b);
	}

}
